namespace FlaUI.Core.Definitions
{
    /// <summary>
    /// Contains values that specify the visual state of a window.
    /// </summary>
    public enum WindowVisualState
    {
        /// <summary>
        /// The window is normal (restored).
        /// </summary>
        Normal = 0,

        /// <summary>
        /// The window is maximized.
        /// </summary>
        Maximized = 1,

        /// <summary>
        /// The window is minimized.
        /// </summary>
        Minimized = 2
    }
}
